package com.java.inheritance;

public class First {
	 private String name;
	    private int Id;
		
	     //Constructor for these methods.
	     public First(String name, int id) {
			super();
			this.name = name;
			this.Id = id;
	    }
	     
	     //Getter and Setter
	     public String getName() {
	 		return name;
	 	}
	 	public void setName(String name) {
	 		this.name = name;
	 	}
	 	public int getId() {
	 		return Id;
	 	}
	 	public void setId(int id) {
	 		Id = id;
	 	} 
}   
