package com.java.encapsulation;

public class Encapsulation {
	 private int model;
     private String name;
     
	public int getModel() {
		return model;
	}
	public void setModel(int model) {
		this.model = model;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	} 
}
